function out = manyCol(n)

%manyCol(n) gives a 3xn matrix of color values. 
%These color values are supposed to be maximally distinct.
%If n>24, then the 25th through nth column are just black.

theCols=[0, 0, 0;...
0, 0, 255;...
255, 0, 0;...
0, 255, 0;...
255, 0, 255;...
128, 128, 128;...
128, 0, 0;...
255, 128, 0;...
255, 255, 0;...
94,241,242;...
0,153,143;...
255,168,187;...
0,92,49;...
224,255,102;...
116,10,255;...
193, 0, 32;...
166, 189, 215;...
147, 170, 0;...
153, 126, 121;...
224, 176, 255;...
184, 134, 11;...
201, 255, 229;...
215, 215, 190;...
224, 171, 118];


theCols=theCols/255;

if(n<=length(theCols))
    out=theCols(1:n,:);
else
    out=[theCols;repmat([0 0 0],(n-length(theCols)),1)];
end

return

%The code here is useful if you want to see what the colors look like next to each other.  To access it, comment out the return in the above line.

close all
figure()
hold on
for(i=1:n)
    i
    x=[0:.001:1];
    plot(x,x*0+.1*i,'o','Color',out(i,:))
    hold on
    %pause
end
